print("Calculadora de Volumen")
# Ingreso de datos
altura = float(input("Ingrese la altura de su cuarto(en metros):"))
ancho = float(input("Ingrese el ancho de su cuarto(en metros):"))
profundidad = float(input("Ingrese la profundidad de su cuarto(en metros):"))

# Proceso
volumen = altura * ancho * profundidad

# Mostrar resultados
print("El volumen es de", volumen, "m3")
